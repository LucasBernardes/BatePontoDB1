import UIKit
extension UIColor {
    
    static func vermelhoClaro() -> UIColor? {
        return UIColor.init(named: "Vermelho Claro")
    }
    
    static func vermelhoEscuro() -> UIColor? {
        return UIColor.init(named: "Vermelho Escuro")
    }
    
    
    
}
